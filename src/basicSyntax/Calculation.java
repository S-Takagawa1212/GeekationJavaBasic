package basicSyntax;

public class Calculation {

	public static void main(String[] args) {
		// 練習1
		int x = 5;
		int y = 10;
		int z = x + y;
		System.out.println(z);
		
		// 練習2
		double a = 5.25;
		a += 2.5;
		a *= 3;
		int b = (int) a;
		b += 10;
		
		System.out.println(a);
		System.out.println(b);
				

	}

}
